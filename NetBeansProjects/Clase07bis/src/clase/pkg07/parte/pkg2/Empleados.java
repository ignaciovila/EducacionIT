    public boolean validar() {
        boolean x = true;

        // Legajo
        try {
            Integer.parseInt(txtLegajo.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "El legajo debe ser un numero");
            x = false;
            txtLegajo.setText("");
            txtLegajo.requestFocus();
            return x;
        }

        // edad
        try {
            Integer.parseInt(txtEdad.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "La edad debe ser un numero");
            x = false;
            txtEdad.setText("");
            txtEdad.requestFocus();
            return x;
        }

        return x;

    }

