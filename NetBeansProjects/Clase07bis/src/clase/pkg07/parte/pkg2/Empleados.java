    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (validar()) {
            e = new Empleado(Integer.parseInt(txtLegajo.getText()), txtNombre.getText(),
                    txtApellido.getText(), txtDireccion.getText(), Integer.parseInt(txtEdad.getText()));
            String x = "";
            x += e.getLegajo() + "\t" + e.getNombre() + "\t" + e.getApellido() + "\t" + e.getDireccion() + "\t"
                    + e.getEdad();
            try {
                Archivo.escribirArchivo(x);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Se ha producido un error");
            }

            limpiar();
            cargar();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

