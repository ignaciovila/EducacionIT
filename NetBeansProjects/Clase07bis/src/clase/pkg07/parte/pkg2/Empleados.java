    public void limpiar() {
        txtLegajo.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtDireccion.setText("");
        txtEdad.setText("");
        txtLegajo.requestFocus();
    }

    public void cargar() {
        String x;
        String cadena = "";
        try {
            Archivo.abrirArchivo();
            x = Archivo.leerLinea();
            while (x != null) {
                cadena += x + "\n";
                x = Archivo.leerLinea();
                jtaLista.setText(cadena);
                Archivo.cerrarArchivo();
            }
        } catch (Exception e) {
        }
    }
